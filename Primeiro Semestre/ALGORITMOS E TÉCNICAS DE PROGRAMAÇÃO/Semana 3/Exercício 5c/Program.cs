// Faça um programa que escreva número inteiros de 10 até 1 utilizando estrutura de repetição com Variável de Controle: for.
using System;

class program
{
    static void Main()
    {
        Console.WriteLine("esse programa conta de 1 até 10.");
        
        for(int x = 10; x > 0; x--)
        {
            Console.WriteLine(x);
        }  
    }
}

