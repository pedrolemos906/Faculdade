// Faça um programa que escreva número inteiros de 10 até 1 utilizando estrutura de repetição com teste no inicio: while.
using System;
class program
{
    static void Main()
    {
        int x = 11;

        Console.WriteLine("esse programa conta de 1 até 10.");
        while (x > 1)
        {
            x--; 
            Console.WriteLine(x);   
        }     
    }
}

