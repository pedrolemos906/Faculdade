// Faça um programa que escreva número inteiros de 1 até 10 utilizando estrutura de repetição com Variável de Controle: for.
using System;

class program
{
    static void Main()
    {
        int x;
        Console.WriteLine("esse programa conta de 1 até 10.");
        for (x = 1 ; x < 11; x++)
        {
            Console.WriteLine(x);      
        }
    }
}

