// Faça um programa que escreva número inteiros de 1 até 10 utilizando estrutura de repetição com teste no inicio: while.
using System;


class program
{
    static void Main()
    {
        int x = 0;

        Console.WriteLine("esse programa conta de 1 até 10.");
        while (x < 10)
        {
            x++; 
            Console.WriteLine(x);   
        }
            
            
    }
}

